#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, t_size n)
{
	t_size i;
	const char *str1 = (void *)s1;
	const char *str2 = (void *)s2;

	i = 0;
	while (str1[i] == str2[i] && i < n - 1)
		i++;
	return (str1[i] - str2[i]);
}